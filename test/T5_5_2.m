%散布常数对径向基函数网络设计的影响
%一般情况下，散布常数的选取取决于输入向量之间的距离，要求大于最小距离，小于最大距离

%1、欠交叠神经元
%如果径向基神经元的散布常数过小，网络设计中将需要更多的神经元数目
P = -1:0.1:1;
T = [-.9602 -.5770 -.0729 .3771 .6405 .6600 .4609...
    .1336 -.2013 -.4344 -.5000 -.3930 -.1647 .0988...
    .3072 .3960 .3449 .1816 -.0312 -.2189 -.3201];

plot(P,T,'+');
title('训练样本');
xlabel('输入向量P');
ylabel('目标向量T');

eg = 0.02;
sc = .01;
net = newrb(P,T,eg,sc);

X = -1:0.01:1;
Y = sim(net,X);
hold on;
plot(X,Y);
hold off;




