%散布常数对径向基函数网络设计的影响
%一般情况下，散布常数的选取取决于输入向量之间的距离，要求大于最小距离，小于最大距离


%2、交叠神经元
%如果径向基神经元的散布常数过大，则每一个神经元都基本相同，应用
%newrb()函数无法进行网络设计
P = -1:0.1:1;
T = [-.9602 -.5770 -.0729 .3771 .6405 .6600 .4609...
    .1336 -.2013 -.4344 -.5000 -.3930 -.1647 .0988...
    .3072 .3960 .3449 .1816 -.0312 -.2189 -.3201];

plot(P,T,'+');
title('训练样本');
xlabel('输入向量P');
ylabel('目标向量T');

eg = 0.02;
sc = 100;
net = newrb(P,T,eg,sc);

%交叠性过大，神经元的输出均为1，无法产生不同的响应曲线

Y = sim(net,P);
hold on;
plot(P,Y);
hold off;
