%outstar学习规则
%输出向量与权值向量相似度

%输入向量
P = [1 0];
%期望输出矩阵
T = [0.1826 0.6325;0.3651 0.3162;0.5477 0.3162;0.7303 0.6325];

%网络训练的结果是，使其权值矩阵趋于所有对应于输入为1时的期望输入向量
[R,Q] = size(P);
[S,Q] = size(T);
W = zeros(S,R);
max_epoch = 10;
lp.lr = 0.3;

%学习训练
for epoch = 1:max_epoch
    for q=1:Q
        A = T(:,q);
        dW=learnos(W,P(:,q),[],[],A,[],[],[],[],[],lp,[]);
        W=W+dW;
    end
end

%训练结束后，当外星工作室，对于输入为1的向量，将能够回忆起被记忆在网络中的第一个期望输入向量的近似值
Ptest =[1];
A=purelin(W*Ptest);
